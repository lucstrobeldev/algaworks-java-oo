package fundamentos_linguagem.promocao_aritmetica;

public class PromocaoAritmetica {

    public static void main(String[] args) {
        int x = 3;
        long y = 2;
        long z = x + y;

        float n = x / (float) y;

        System.out.println(z);
        System.out.println(n);
    }
}
